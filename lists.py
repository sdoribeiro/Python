# define a list of names
names = ["Sandro ", "Rafael", "Felipe", "Daniele"]
print(names)
print(f"Name[1]: {names[0]}")
print(f"Name[2]: {names[1]}")
print(f"Name[3]: {names[2]}")
print(f"Name[4]: {names[3]}")

# add elements to a list
names.append("Snow")
print(names)
print(f"Name[5]: {names[4]}")

# order a list
names.sort()
print(names)